<h2 id="survivors">Survivors</h2>

<p>The main goal of a survivor is to stay nourished. A survivor has to keep their hearts as full as possible by solving clues and entering the reward codes onto the Metazombies website or by competing in the minigames.
Secondary to this, a survivor must avoid being touched by a zombie (see capture) as once a zombie has claimed a survivor, said survivor becomes a zombie.</p>

<h2 id="zombies">Zombies</h2>

<p>The main goal of zombies is to capture humans in order to remain active.
Secondary to this, a zombie must try and avoid being stunned.</p>

<h2 id="alphazombies">Alpha Zombies</h2>

<p>There are a handful of players designated as the alpha zombies. Alpha zombies are the first of zombie kind and have different attributes to normal zombies. These attributes might change and will be displayed on the website. Be sure to stay informed.</p>

<p>The first and most important characteristic of an alpha zombie is that they may capture survivors without having to wear any headbands/apparel</p>

<h2 id="capture">Capture</h2>

<p>The core mechanic behind Metazombies is capture. This is what makes the game exciting. Zombies (except for alpha zombies) are only allowed to capture survivors while they are wearing their council-issued headbands. At the time of capture, the headbands must be fully around the forehead and visible. Zombies can remove their headbands but are effectively logged out until they put them back on.
In order to capture a survivor the zombie may not be stunned, and must place both hands fully on the victim in order to confirm capture (no holding)</p>

