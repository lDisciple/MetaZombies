<h2 id="interference">Interference</h1>

<p>Players and non-players may not interfere with survivors in such a way that prevents them from fairly escaping, i.e. holding a friend for another friend to grab. Zombies may, however corner survivors. Beware. The council has final say over any disputes that arise from interference conflicts.</p>

